# Strings Slicing/Indexing
# Basically accessing specific elements of an list or string

phone_num = "605-671-8620"

#To print all the strings
print(phone_num[0:])

#To print first five:
print(phone_num[0:6]) #last limit is exclsive

#first to last with 2 steps
print(phone_num[0::2])

#Last element
print(phone_num[-1])

#Reverse
print(phone_num[::-1])

#Last five
print(phone_num[-4::1])